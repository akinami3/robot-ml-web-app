// =============================================================================
// PostCSS 設定ファイル（postcss.config.js）
// =============================================================================
//
// 【PostCSSとは？】
// CSSを加工・変換するためのツールです。プラグインを追加することで、
// 様々なCSS処理を自動的に行えます。
// 例: ベンダープレフィックス自動追加、最新CSS構文の変換、圧縮など
//
// 【なぜPostCSSが必要？】
// 1. TailwindCSS: ユーティリティクラスをCSSに変換するために必要
// 2. Autoprefixer: 各ブラウザ向けのプレフィックスを自動追加
//
// Viteは内部的にPostCSSを使用し、この設定ファイルを自動で読み込みます。
// CSSファイルが読み込まれるたびに、以下のプラグインが順番に適用されます。
// =============================================================================

export default {
  // 【plugins】使用するPostCSSプラグインの設定
  // プラグインは上から順番にCSSを処理します
  plugins: {
    // 【tailwindcss】TailwindCSSプラグイン
    // TailwindCSSのユーティリティクラス（例: bg-blue-500, text-lg, flex）を
    // 実際のCSSプロパティに変換します
    //
    // 処理の流れ:
    //   1. ソースコード内で使われているTailwindクラスを検出
    //   2. 使われているクラスのCSSだけを生成（Tree Shaking）
    //   3. 未使用のクラスは生成しない → CSSファイルが小さくなる
    //
    // ※ {} は空のオプション（デフォルト設定を使用）
    tailwindcss: {},

    // 【autoprefixer】ベンダープレフィックス自動追加プラグイン
    // ブラウザごとの互換性を確保するために、CSSプロパティに
    // プレフィックスを自動で追加します
    //
    // 例:
    //   入力:  display: flex;
    //   出力:  display: -webkit-flex;  ← Safari用
    //          display: flex;
    //
    //   入力:  user-select: none;
    //   出力:  -webkit-user-select: none;  ← Safari用
    //          -moz-user-select: none;     ← Firefox用
    //          user-select: none;
    //
    // これにより、開発者はプレフィックスを気にせずに
    // 標準的なCSSプロパティだけを書けばよくなります
    autoprefixer: {},
  },
};
